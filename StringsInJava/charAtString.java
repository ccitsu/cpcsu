package StringsInJava;
public class charAtString{
          public static void main(String args[]) {
                    String s = "Strings are immutable";
                    char result = s.charAt(8);
                    System.out.println(result);
                    result = s.charAt(10);
                    System.out.println(result);
                    result = s.charAt(3);
                    System.out.println(result);
                    }  
}
          